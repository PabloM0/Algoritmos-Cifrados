    private void btnCifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCifrarActionPerformed
        
        try {
            rc4KeyGenerator = KeyGenerator.getInstance(ENCRYPTION_ALGORITHM);
            secretKey = rc4KeyGenerator.generateKey();
            rc4 = Cipher.getInstance(ENCRYPTION_ALGORITHM);
            
            String plano = txtPlano.getText();
            
            en = encrypt(plano, secretKey, rc4);
            
            String encriptado = new String(en);
            
            txtCifrado.setText(encriptado);
            
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(FrameRC4.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchPaddingException ex) {
            Logger.getLogger(FrameRC4.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidKeyException ex) {
            Logger.getLogger(FrameRC4.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalBlockSizeException ex) {
            Logger.getLogger(FrameRC4.class.getName()).log(Level.SEVERE, null, ex);
        } catch (BadPaddingException ex) {
            Logger.getLogger(FrameRC4.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCifrarActionPerformed

